E.label2code = function(label){
    var c_list = Object.entries(E.list).concat(Object.entries(E.google_list));
    for (var i = 0; i<c_list.length; i++)
    {
        var _c = c_list[i];
        if (_c[1]===label)
            return _c[0];
        var bracket_ind = _c[1].indexOf('(');
        if (bracket_ind!=-1 && _c[1].slice(0, bracket_ind-1)==label)
            return _c[0];
    }
    // Åland Islands case
    for (var k in E.adj_list)
    {
        if (E.adj_list[k].name==label)
            return k;
    }
    var exc = {
        'Macedonia (FYROM)': 'MK',
    };
    return exc[label] || '';
};

